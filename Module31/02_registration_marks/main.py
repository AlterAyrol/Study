import re


text = "А578ВЕ777 ОР233787 К901МН666 СТ46599 СНИ2929П777 666АМР666'"


passenger = re.findall(r'\b[АВЕКМНОРСТУХ]\d{3}[АВЕКМНОРСТУХ][АВЕКМНОРСТУХ]\d{2,3}', text)
print('Список номеров частных автомобилей:', passenger)

taxi = re.findall(r'\b[АВЕКМНОРСТУХ][АВЕКМНОРСТУХ]\d{3}\d{2,3}', text)
print('Список номеров такси:', taxi)