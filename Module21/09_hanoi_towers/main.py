
def move(n, A, B, C):
    if n == 1:
        print(f"Переложить диск {n} со стержня номер {A} на стержень номер {B}")
        return
    move(n - 1, A, C, B)
    print(f"Переложить диск {n} со стержня номер {A} на стержень номер {B}")
    move(n - 1, C, B, A)


